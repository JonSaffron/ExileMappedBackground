        public int? GetBackground(byte squareX, byte squareY)
            {
            byte accumulator;
            byte xreg;
            byte yreg;
            bool carry;

            accumulator = squareY;                              // LDA square_y
            xreg = accumulator;                                 // TAX
            ArithmeticShiftRight(ref accumulator, out carry);   // LSR A
            accumulator = (byte) (accumulator ^ squareX);       // EOR square_x
            accumulator = (byte) (accumulator & 0xF8);          // AND #$F8
            ArithmeticShiftRight(ref accumulator, out carry);   // LSR A
            AddWithCarry(ref accumulator, squareX, ref carry);       // ADC square_x
            ArithmeticShiftRight(ref accumulator, out carry);   // LSR A
            AddWithCarry(ref accumulator, squareY, ref carry);       // ADC square_y
            byte zp_various_9d = accumulator;                   // zp_various_9d    ; f_xy is a function of square_x and square_y
            accumulator = xreg;                                 // TXA              ; A = square_y
            carry = accumulator >= 0x79;                        // CMP #$79
            if (!carry) goto L17A8;                             // BCC L17A8        ; taken if square_y<0x79
            carry = accumulator >= 0xBF;                        // CMP #$BF
            if (!carry) goto not_mapped2;                       // BCC not_mapped2  ; taken if square_y<0xbf

            // before: square_y = 0xc0 ... 0xff
            // after:  square_y = 0x7a ... 0xb9
            SubtractWithBorrow(ref accumulator, 0x46, ref carry);   // SBC #$46         ; y >= &c0 ; y -= &46;
    
L17A8:
            // A = square_y
            carry = accumulator >= 0x48;                        // CMP #$48
            if (carry) goto L17B2;                              // BCS L17B2        ; taken if square_y>=0x48
            carry = accumulator >= 0x3E;                        // CMP #$3E
            if (carry) goto not_mapped2;                        // BCS not_mapped2  ; taken if square_y>=0x3e

            // before: square_y = 0x00 ... 0x3d
            // after:  square_y = 0x0a ... 0x47
            AddWithCarry(ref accumulator, 0x0A, ref carry);         // ADC #$0A
    
L17B2:
            byte f2_xy = accumulator;                           // STA f2_xy        ; at this point, a function of square_y
            accumulator = (byte) (accumulator & 0b10101000);    // AND #%10101000                   
            accumulator = (byte) (accumulator ^ 0b01101111);    // EOR #%01101111
            ArithmeticShiftRight(ref accumulator, out carry);   // LSR A
            AddWithCarry(ref accumulator, squareX, ref carry);      // ADC square_x
            accumulator = (byte) (accumulator ^ 0b01100000);    // EOR #%01100000
            AddWithCarry(ref accumulator, 0b00101000, ref carry);   // ADC #%00101000
            byte f3_xy = accumulator;                           // STA f3_xy        ; f3_xy is another function of square_x and square_y
            accumulator = (byte) (accumulator & 0b00111000);    // AND #%00111000
            accumulator = (byte) (accumulator ^ 0b10100100);    // EOR #%10100100
            AddWithCarry(ref accumulator, f2_xy, ref carry);        // ADC f2_xy
            f2_xy = accumulator;                                // STA f2_xy        ; f2_xy a function of square_x and square_y
            yreg = accumulator;                                 // TAY              ; A is f2_xy
            accumulator = (byte) (accumulator ^ 0b00101100);    // EOR #%00101100
            AddWithCarry(ref accumulator, f3_xy, ref carry);        // ADC f3_xy      ; A is a function of f2_xy and f3_xy
            carry = yreg >= 0x20;                               // CPY #$20
            if (carry) goto not_mapped2;                        // BCS not_mapped2        ; taken if f2_xy>=0x20
            carry = accumulator >= 0x20;                        // CMP #$20
            if (carry) goto not_mapped;                         // BCS not_mapped         ; if A >= &20, don't use mapped data

            bool square_is_mapped_data = true;                  // DEC square_is_mapped_data
            yreg = accumulator;                                 // TAY                    ; Y = A = f(f2_xy,f3_xy)
            ArithmeticShiftLeft(ref accumulator, out carry);    // ASL A
            ArithmeticShiftLeft(ref accumulator, out carry);    // ASL A
            ArithmeticShiftLeft(ref accumulator, out carry);    // ASL A
            accumulator = (byte) (accumulator ^ f2_xy);         // EOR f2_xy
            byte map_address = accumulator;                     // STA map_address        ; &a4 = (A * 8) ^ &10;
    
            accumulator = yreg;                                 // TYA                    ; A = Y = f(f2_xy,f3_xy)
            accumulator = (byte) (accumulator & 0x03);          // AND #$03
            AddWithCarry(ref accumulator, 0, ref carry);    // ADC #HI(map_data)
            byte map_address_high = accumulator;                // STA map_address_high        ; a5 = (A & 3) + &4f;
    
            yreg = 0;                                           // LDY #LO(map_data)           ; $EC ; &4fec - &53ec
            int result = (map_address_high << 8) + map_address;            // LDA (map_address),Y         ; use mapped data
            return result;                                      // RTS
            
not_mapped:
not_mapped2:
            return null;
            }

        public byte GetMapData(int position, byte squareX, out string log)
            {
            log = $"Position: {position}";
            var result = MapData[position];
            var spriteOrHash = result & 0x3f;
            if (spriteOrHash < 9)
                {
                // check the background objects X lookup list
                var listOfX = BackgroundObjectsXLookup[spriteOrHash];
                var positionInHash = Array.IndexOf(listOfX, squareX);
                if (positionInHash == -1)
                    {
                    log += "\r\nUsing UnmatchedHash";
                    result = LookupForUnmatchedHash[spriteOrHash];
                    }
                else
                    {
                    log += "\r\nUsing HandlerLookup";
                    var handlerList = BackgroundObjectsHandlerLookup[spriteOrHash];
                    result = handlerList[positionInHash];
                    }
                }

            var spriteOrBackgroundHandler = result & 0x3f;
            if (spriteOrBackgroundHandler < 0x10)
                {
                switch (spriteOrBackgroundHandler)
                    {
                    case 0:
                        log += "\r\nhandle_background_invisible_switch";
                        break;
                    case 1:
                        log += "\r\nhandle_background_teleport_beam";
                        break;
                    case 2:
                        log += "\r\nhandle_background_object_from_data";
                        break;
                    case 3:
                        log += "\r\nhandle_background_door";
                        break;
                    case 4:
                        log += "\r\nhandle_background_stone_door";
                        break;
                    case 5:
                    case 6:
                    case 7:
                        log += "\r\nhandle_background_object_from_type";
                        break;
                    case 8:
                        log += "\r\nhandle_background_switch";
                        break;
                    case 9:
                    case 0xA:
                        log += "\r\nhandle_background_object_emerging";
                        break;
                    case 0xB:
                        log += "\r\nhandle_background_object_fixed_wind";
                        break;
                    case 0xC:
                        log += "\r\nhandle_background_engine_thruster";
                        break;
                    case 0xD:
                        log += "\r\nhandle_background_object_water";
                        break;
                    case 0xE:
                        log += "\r\nhandle_background_object_random_wind";
                        break;
                    case 0xF:
                        log += "\r\nhandle_background_mushrooms";
                        break;
                    }
                }

            return result;
            }

        void AddWithCarry(ref byte accumulator, byte data, ref bool carry)
            {
            int result = accumulator + data + (carry ? 1 : 0);
            accumulator = (byte) (result % 256);
            carry = result > 255;
            }

        void SubtractWithBorrow(ref byte accumulator, byte data, ref bool carry)
            {
            int result = accumulator - data - (carry ? 0 : 1);
            if (result < 0)
                {
                carry = false;
                result += 256;
                }
            else
                {
                carry = true;
                }
            accumulator = (byte) (result % 256);
            }

        void ArithmeticShiftRight(ref byte accumulator, out bool carry)
            {
            carry = (accumulator & 1) != 0;
            accumulator = (byte) (accumulator >> 1);
            }

        void ArithmeticShiftLeft(ref byte accumulator, out bool carry)
            {
            carry = (accumulator & 0x80) != 0;
            accumulator = (byte) (accumulator << 1);
            }    


