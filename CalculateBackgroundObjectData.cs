        private static byte[] BuildLookupForUnmatchedHash()
            {
            return new byte[]
                {
                0x1B,   // list 0 - tree 
                0x5A,   // list 1 - half a tree
                0x19,   // list 2 - empty square
                0x19,   // list 3 - empty square
                0x1E,   // list 4 - full wall
                0x13,   // list 5 - wall with a corner missing
                0x24,   // list 6 - sloped wall
                0x2C,   // list 7 - portion of earth with grass edging
                0x19    // list 8 - empty square
                };
            }

