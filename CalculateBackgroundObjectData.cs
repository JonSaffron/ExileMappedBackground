        private static readonly List<byte[]> BackgroundObjectsXLookup = BuildBackgroundObjectsXLookup();
        private static readonly byte[] LookupForUnmatchedHash = BuildLookupForUnmatchedHash();
        private static readonly List<byte[]> BackgroundObjectsHandlerLookup = BuildBackgroundObjectsHandlerLookup();
        private static readonly List<byte[]> BackgroundDataLookup = BuildBackgroundDataLookup();
        private static readonly List<byte[]> BackgroundTypeLookup = BuildBackgroundTypeLookup();
        public static readonly List<BackgroundObject> BackgroundObjectList = BuildBackgroundObjectList();

        internal static BackgroundOverride? GetBackgroundObjectData(byte calculatedBackground, byte squareX)
            {
            var overrideListId = (byte) (calculatedBackground & 0x3f);
            if (overrideListId >= 9)
                {
                return null;
                }

            // check the background objects X lookup list
            var listOfX = BackgroundObjectsXLookup[overrideListId];
            var positionInHash = Array.IndexOf(listOfX, squareX);
            if (positionInHash == -1)
                {
                byte defaultBackground = LookupForUnmatchedHash[overrideListId];
                defaultBackground ^= (byte) (calculatedBackground & 0xc0);
                return new BackgroundOverride(overrideListId, defaultBackground);
                }

            byte backgroundObjectId = (byte) positionInHash;
            for (int i = 0; i < overrideListId; i++)
                {
                var countOfItemsInHash = BackgroundObjectsXLookup[i].Length;
                backgroundObjectId += (byte) countOfItemsInHash;
                }

            var result = BackgroundObjectList[backgroundObjectId];
            return result;
            }

        private static List<BackgroundObject> BuildBackgroundObjectList()
            {
            var result = new List<BackgroundObject>();
            byte dataIndex = 0;
            for (byte listIndex = 0; listIndex <= 8; listIndex++)
                {
                var list = BackgroundObjectsXLookup[listIndex];
                var contentList = BackgroundObjectsHandlerLookup[listIndex];
                var dataList = BackgroundDataLookup[listIndex];
                var typeList = BackgroundTypeLookup[listIndex];

                for (int itemIndex = 0; itemIndex < list.Length; itemIndex++)
                    {
                    byte content = contentList[itemIndex];

                    byte? data = itemIndex < dataList.Length ? dataList[itemIndex] : new byte?();
                    if (data.HasValue)
                        {
                        dataIndex += 1;
                        }

                    byte? objectType = itemIndex < typeList.Length ? typeList[itemIndex] : new byte?();

                    if (!data.HasValue)
                        {
                        // scenery or a background event that requires no state
                        Debug.Assert(objectType == null);
                        var backgroundObject = new BackgroundObject(listIndex, (byte)result.Count, content);
                        result.Add(backgroundObject);
                        continue;
                        }

                    var backgroundEvent = new BackgroundEvent(listIndex, (byte) result.Count, content, dataIndex, data.Value, objectType);
                    result.Add(backgroundEvent);
                    }
                }

            return result;
            }

